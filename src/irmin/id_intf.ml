module type S = sig
  include Type.S

  type hash

  val short_hash : t -> int
  val to_hash : t -> hash
  val of_hash : hash -> t

  module Hash : Hash.S with type t = hash
end

module type Id = sig
  module type S = S

  module Identity (H : Hash.S) : S with type t = H.t and type hash = H.t
end
